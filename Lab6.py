ia=-3
ib=4
fa=4.1
fb=0.43
# int & int
print(f"{ia} ** {ib}={ia**ib}")
print(f"{ia} / {ib}={ia/ib}")
print(f"{ia} // {ib}={ia//ib}")
print(f"{ia} % {ib}={ia%ib}")
# float & float
print(f"{fa} ** {fb}={fa**fb}")
print(f"{fa} / {fb}={fa/fb}")
print(f"{fa} // {fb}={fa//fb}")
print(f"{fa} % {fb}={fa%fb}")
# int & float
print(f"{fa} ** {ia}={fa**ia}")
print(f"{fa} / {ia}={fa/ia}")
print(f"{fa} // {ia}={fa//ia}")
print(f"{fa} % {ia}={fa%ia}")
